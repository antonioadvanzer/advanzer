  <h3>4.-       Procedimiento</h3>
  <p>
    <ol type="a">
      <li>El empleado al estar interesado en tomar alguna Academia de SAP deberá solicitar validación de parte de su Jefe Directo que la misma Academia de interés esté relacionada con sus funciones actuales y/o futuras a través de un correo electrónico.</li>
      <li>El mismo correo se reenviará a Capital Humano, en donde se validarán las fechas y modalidades disponibles para la Academia deseada y que el empleado cumpla los requisitos.</li>
      <li>Capital Humano informará a través del Formato de Autorización de Academia SAP (Anexo) los términos dentro de los cuales cursará la Academia y del apoyo que Advanzer proporcionaría.</li>
      <li>Al obtener la validación del empleado, Capital Humano comenzará la gestión de los trámites administrativos correspondientes incluyendo la firma de pagaré por la cantidad total del costo de la Academia.</li>
    </ol>
  </p>
  <h3>5.-      Examen de certificación</h3>
  <p>En la mayoría de las Academias existen 2 oportunidades de presentar el examen de certificación incluidas en el costo de la Academia. En caso de no aprobar el examen en el número de intentos otorgados por el partner educativo de SAP, el empleado absorberá el 100% del costo de los exámenes necesarios para certificarse.</p>
