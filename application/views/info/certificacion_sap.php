  <p>Establecer de manera clara los criterios a través de los cuales Advanzer apoyará a sus empleados en obtener mejor 
    preparación y Certificación a través de Academias de SAP.</p>
  <h3>2.-       Alcance</h3>
  <p>Para todo el personal de Advanzer que preste sus servicios dentro del área de Consultoría y que cuente con 
